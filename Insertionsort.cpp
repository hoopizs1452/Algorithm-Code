#include <iostream>
using namespace std;

int *Insertionsort(int arr[], int size) {
	//int size = sizeof(arr) / sizeof(arr[0]);
	for (int i = 1; i < size; i++) {
		int key = arr[i];
		int j = i - 1;
		while (j >= 0 && arr[j] > key) {
			arr[j + 1] = arr[j];
			j -= 1;
		}
		arr[j + 1] = key;
	}
	return arr;
}

int main()
{
	int arr[6] = { 4, 6, 5, 2, 3, 1 };
	int *ptr = Insertionsort(arr, 6);
	for (int i = 0; i < 6; i++) {
		cout << ptr[i] << " ";
	}
	cout << endl;
}

